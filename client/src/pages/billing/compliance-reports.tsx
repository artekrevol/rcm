function ReportCard({ report }: { report: typeof REPORTS[0] }) {
  const { toast } = useToast();
  const [startDate, setStartDate] = useState(format(subDays(new Date(), 30), "yyyy-MM-dd"));
  const [endDate, setEndDate] = useState(format(new Date(), "yyyy-MM-dd"));
  const [generating, setGenerating] = useState(false);

  async function handleGenerate() {
    setGenerating(true);
    try {
      const params = new URLSearchParams({ startDate: new Date(startDate).toISOString(), endDate: new Date(endDate + "T23:59:59").toISOString() });
      const res = await fetch(`/api/billing/compliance-report/${report.id}?${params}`, { credentials: "include" });
      if (!res.ok) throw new Error("Failed to fetch report data");
      const data = await res.json();
      const rows = data.map(report.mapRow);
      const dateRange = `${format(new Date(startDate), "MMM d, yyyy")} – ${format(new Date(endDate), "MMM d, yyyy")}`;

      const doc = createElement(ReportDocument, { title: report.title, dateRange, columns: report.columns, rows });
      const blob = await pdf(doc).toBlob();
      const url = URL.createObjectURL(blob);
      const a = document.createElement("a");
      a.href = url;
      a.download = `${report.id}_report_${startDate}_${endDate}.pdf`;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      URL.revokeObjectURL(url);

      toast({ title: `${report.title} downloaded`, description: `${data.length} records included.` });
    } catch (err: any) {
      toast({ title: "Report generation failed", description: err.message, variant: "destructive" });
    } finally {
      setGenerating(false);
    }
  }

  const Icon = report.icon;

  return (
    <Card data-testid={`card-report-${report.id}`}>
      <CardHeader className="pb-3">
        <div className="flex items-center gap-3">
          <div className="h-10 w-10 rounded-lg bg-muted flex items-center justify-center">
            <Icon className="h-5 w-5 text-muted-foreground" />
          </div>
          <div>
            <CardTitle className="text-base">{report.title}</CardTitle>
            <CardDescription className="text-xs mt-0.5">{report.description}</CardDescription>
          </div>
        </div>
      </CardHeader>
      <CardContent className="pt-0">
        <div className="flex flex-wrap items-end gap-3">
          <div className="space-y-1">
            <Label className="text-xs">From</Label>
            <Input type="date" value={startDate} onChange={(e) => setStartDate(e.target.value)} className="w-36" data-testid={`input-${report.id}-start`} />
          </div>
          <div className="space-y-1">
            <Label className="text-xs">To</Label>
            <Input type="date" value={endDate} onChange={(e) => setEndDate(e.target.value)} className="w-36" data-testid={`input-${report.id}-end`} />
          </div>
          <Button onClick={handleGenerate} disabled={generating} size="sm" className="gap-1.5" data-testid={`button-generate-${report.id}`}>
            {generating ? <Loader2 className="h-4 w-4 animate-spin" /> : <FileText className="h-4 w-4" />}
            {generating ? "Generating..." : "Generate PDF"}
          </Button>
        </div>
      </CardContent>
    </Card>
  );
}

