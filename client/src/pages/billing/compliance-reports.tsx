function ReportDocument({ title, dateRange, columns, rows }: { title: string; dateRange: string; columns: { label: string; width: number }[]; rows: any[][] }) {
  return (
    <Document>
      <Page size="LETTER" style={pdfStyles.page}>
        <View style={pdfStyles.header}>
          <Text style={pdfStyles.title}>{title}</Text>
          <Text style={pdfStyles.subtitle}>Period: {dateRange} | Generated: {format(new Date(), "MMM d, yyyy h:mm a")}</Text>
        </View>
        <View style={pdfStyles.divider} />
        {rows.length > 0 ? (
          <>
            <View style={pdfStyles.tableHeader}>
              {columns.map((col, i) => (
                <Text key={i} style={[pdfStyles.tableHeaderCell, { width: col.width }]}>{col.label}</Text>
              ))}
            </View>
            {rows.slice(0, 100).map((row, ri) => (
              <View key={ri} style={pdfStyles.tableRow}>
                {row.map((cell: string, ci: number) => (
                  <Text key={ci} style={[pdfStyles.tableCell, { width: columns[ci]?.width || 80 }]}>{cell}</Text>
                ))}
              </View>
            ))}
            {rows.length > 100 && <Text style={pdfStyles.emptyText}>...and {rows.length - 100} more records</Text>}
          </>
        ) : (
          <Text style={pdfStyles.emptyText}>No records found for this period.</Text>
        )}
        <View style={pdfStyles.footer}>
          <Text style={pdfStyles.footerText}>Generated by ClaimShield Health | {title} | {dateRange}</Text>
        </View>
      </Page>
    </Document>
  );
}

